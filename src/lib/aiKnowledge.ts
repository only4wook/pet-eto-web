// 질문에서 증상 가이드 찾기
export function findSymptomGuide(query: string): SymptomGuide | null {
  const q = query.toLowerCase();
  for (const guide of SYMPTOM_GUIDES) {
    for (const kw of guide.keywords) {
      if (q.includes(kw)) return guide;
    }
  }
  return null;
}

// 증상 가이드를 채팅 응답으로 포맷
export function formatSymptomResponse(guide: SymptomGuide, query: string): string {
  const isCat = query.includes("고양이") || query.includes("묘") || query.includes("냥");
  const animal = isCat ? "고양이" : "강아지";

  let resp = `🔍 ${animal} ${guide.title} — AI 분석\n\n`;

  resp += `📋 가능한 원인:\n`;
  // 관련성 높은 원인 3~4개만 표시
  const relevantCauses = guide.causes.filter((c) => {
    if (isCat && c.includes("소형견")) return false;
    if (!isCat && c.includes("고양이")) return false;
    return true;
  }).slice(0, 4);
  relevantCauses.forEach((c, i) => { resp += `${i + 1}. ${c}\n`; });

  resp += `\n🏠 집에서 할 수 있는 것:\n`;
  guide.homecare.slice(0, 3).forEach((h) => { resp += `• ${h}\n`; });

  resp += `\n🚨 병원 가야 할 때:\n${guide.warning}\n`;
  resp += `\n💡 ${guide.hospital}`;

  return resp;
}
