// 질문에서 증상 가이드 찾기 (가장 많이 매칭되는 가이드 우선)
export function findSymptomGuide(query: string): SymptomGuide | null {
  const q = query.toLowerCase();

  // 각 가이드별 매칭 점수 계산
  let bestGuide: SymptomGuide | null = null;
  let bestScore = 0;

  for (const guide of SYMPTOM_GUIDES) {
    let score = 0;
    for (const kw of guide.keywords) {
      if (q.includes(kw)) {
        // 긴 키워드일수록 높은 점수 (정확도)
        score += kw.length;
      }
    }
    if (score > bestScore) {
      bestScore = score;
      bestGuide = guide;
    }
  }

  if (bestGuide) return bestGuide;

  // 유사도 fallback: 4글자 이상 키워드의 앞부분 매칭 (짧은 키워드 오매칭 방지)
  for (const guide of SYMPTOM_GUIDES) {
    for (const kw of guide.keywords) {
      if (kw.length >= 4 && q.includes(kw.slice(0, 4))) {
        return guide;
      }
    }
  }

  return null;
}

// 증상 가이드를 채팅 응답으로 포맷
export function formatSymptomResponse(guide: SymptomGuide, query: string): string {
  const animal = detectAnimal(query);
  const animalLabel = animal === "cat" ? "고양이" : animal === "dog" ? "강아지" : "반려동물";

  let resp = `🔍 ${animalLabel} ${guide.title} — AI 분석\n\n`;

  // 동물별 원인 + 공통 원인
  resp += `📋 가능한 원인:\n`;

  if (animal === "cat") {
    guide.catCauses.forEach((c, i) => { resp += `${i + 1}. ${c}\n`; });
    if (guide.commonCauses.length > 0) {
      guide.commonCauses.slice(0, 2).forEach((c, i) => { resp += `${guide.catCauses.length + i + 1}. ${c}\n`; });
    }
  } else if (animal === "dog") {
    guide.dogCauses.forEach((c, i) => { resp += `${i + 1}. ${c}\n`; });
    if (guide.commonCauses.length > 0) {
      guide.commonCauses.slice(0, 2).forEach((c, i) => { resp += `${guide.dogCauses.length + i + 1}. ${c}\n`; });
    }
  } else {
    // 종류 불명 — 둘 다 보여줌
    resp += `\n🐶 강아지라면:\n`;
    guide.dogCauses.slice(0, 2).forEach((c) => { resp += `• ${c}\n`; });
    resp += `\n🐱 고양이라면:\n`;
    guide.catCauses.slice(0, 2).forEach((c) => { resp += `• ${c}\n`; });
    resp += `\n공통:\n`;
    guide.commonCauses.slice(0, 2).forEach((c) => { resp += `• ${c}\n`; });
  }

  resp += `\n🏠 집에서 할 수 있는 것:\n`;
  guide.homecare.forEach((h) => { resp += `• ${h}\n`; });

  resp += `\n🚨 병원 가야 할 때:\n${guide.warning}\n`;
  resp += `\n💡 ${guide.hospital}`;

  return resp;
}
