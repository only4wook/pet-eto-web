// 동물 종류 감지 (자연어)
export function detectAnimal(q: string): "cat" | "dog" | "unknown" {
  const catWords = ["고양이","냥이","냥냥","야옹","묘","캣","cat","우리냥","집사","코숏","브숏","페르시안","스코티시","뱅갈","샴","먼치킨","랙돌","노르웨이","셀커크","스핑크스","러시안"];
  const dogWords = ["강아지","멍멍","댕댕","퍼피","독","dog","우리개","우리 개","반려견","말티즈","푸들","골든","포메","시츄","코기","비숑","치와와","요키","닥스훈트","프렌치","시바","보더콜리","래브라도","허스키","진돗개","진도"];
  for (const w of catWords) { if (q.includes(w)) return "cat"; }
  for (const w of dogWords) { if (q.includes(w)) return "dog"; }
  return "unknown";
}

